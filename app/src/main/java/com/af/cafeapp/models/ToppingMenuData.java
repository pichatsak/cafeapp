package com.af.cafeapp.models;

public class ToppingMenuData {
    private String nameTopping;
    private float priceTopping;
    private float priceToppingGrab;
    private float priceToppingFoodPanda;
    private float priceToppingRobinHood;

    public ToppingMenuData() {

    }

    public String getNameTopping() {
        return nameTopping;
    }

    public void setNameTopping(String nameTopping) {
        this.nameTopping = nameTopping;
    }

    public float getPriceTopping() {
        return priceTopping;
    }

    public void setPriceTopping(float priceTopping) {
        this.priceTopping = priceTopping;
    }

    public float getPriceToppingGrab() {
        return priceToppingGrab;
    }

    public void setPriceToppingGrab(float priceToppingGrab) {
        this.priceToppingGrab = priceToppingGrab;
    }

    public float getPriceToppingFoodPanda() {
        return priceToppingFoodPanda;
    }

    public void setPriceToppingFoodPanda(float priceToppingFoodPanda) {
        this.priceToppingFoodPanda = priceToppingFoodPanda;
    }

    public float getPriceToppingRobinHood() {
        return priceToppingRobinHood;
    }

    public void setPriceToppingRobinHood(float priceToppingRobinHood) {
        this.priceToppingRobinHood = priceToppingRobinHood;
    }
}
